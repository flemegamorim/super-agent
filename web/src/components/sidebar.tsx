"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { useSession, signOut } from "next-auth/react";

const NAV_ITEMS = [
  { href: "/", label: "Dashboard", icon: LayoutIcon },
  { href: "/tasks/new", label: "New Task", icon: PlusIcon },
  { href: "/prompts", label: "Prompts", icon: DocIcon },
  { href: "/agents", label: "Agents", icon: BotIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();

  return (
    <aside className="flex w-60 flex-col border-r border-zinc-800 bg-zinc-900">
      <div className="flex h-14 items-center gap-2 border-b border-zinc-800 px-4">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-indigo-600 text-sm font-bold">
          SA
        </div>
        <span className="text-sm font-semibold">Super Agent</span>
      </div>

      <nav className="flex-1 space-y-1 p-3">
        {NAV_ITEMS.map((item) => {
          const isActive =
            item.href === "/"
              ? pathname === "/"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm transition-colors ${
                isActive
                  ? "bg-zinc-800 text-white"
                  : "text-zinc-400 hover:bg-zinc-800/50 hover:text-white"
              }`}
            >
              <item.icon className="h-4 w-4" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="border-t border-zinc-800 p-3">
        <div className="flex items-center gap-2 rounded-lg px-3 py-2">
          <div className="h-2 w-2 rounded-full bg-emerald-500" />
          <span className="text-xs text-zinc-500">OpenCode Server</span>
        </div>
        {session?.user && (
          <div className="mt-1 flex items-center justify-between rounded-lg px-3 py-2">
            <div className="min-w-0">
              <p className="truncate text-xs font-medium text-zinc-300">
                {session.user.name}
              </p>
              <p className="truncate text-xs text-zinc-600">
                {session.user.email}
              </p>
            </div>
            <button
              onClick={() => signOut({ callbackUrl: "/login" })}
              className="ml-2 shrink-0 text-xs text-zinc-500 hover:text-red-400"
              title="Sign out"
            >
              <LogoutIcon className="h-4 w-4" />
            </button>
          </div>
        )}
      </div>
    </aside>
  );
}

